fun main() {
    var age: Byte = 100
    var total: Int = 5000

    var sample: Float = 10.10F
    println(age)
    println(total)
    println(sample)

    var binary: Int  = 0b010101010101

    println(binary)

    var price: Long = 90_000_000L
    println(price)

    var doubleBinary: Double = binary.toDouble()
    println(doubleBinary)


}